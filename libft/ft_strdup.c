#include "libft.h"

char	*ft_strdup(char *s1)
{
	int		i;
	char	*p;

	i = 0;
	p = malloc((ft_strlen(s1) + 1) * sizeof(char));
	if (p == NULL)
		return (NULL);
	while (s1[i] != '\0')
	{
		p[i] = s1[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
